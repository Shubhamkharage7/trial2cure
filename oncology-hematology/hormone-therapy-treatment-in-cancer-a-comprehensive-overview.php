                                                <h2 class="h1 mb-4">How Hormone Therapy Works</h2>
                                                <p>Hormones are chemicals in the body that regulate various functions, including cell growth. In certain cancers, hormones promote the growth of malignant cells. Hormone therapy works in two main ways:</p>
                                                <ol>
                                                    <li>Lowering hormone levels in the body to reduce the fuel source for cancer cells</li>
                                                    <li>Blocking hormone receptors on cancer cells, preventing the hormone from attaching and stimulating growth.</li>
                                                </ol>

                                                <h4>Types of Hormone Therapy</h4>
                                                <h5>For Breast Cancer:</h5>
                                                <p>Estrogen receptor-positive (ER+) and progesterone receptor-positive (PR+) breast cancers rely on estrogen and progesterone to grow. Hormone therapy for breast cancer works by blocking these hormones.</p>
                                                <ul>
                                                    <li>Tamoxifen: A selective estrogen receptor modulator (SERM) that blocks estrogen receptors on breast cancer cells, making it harder for the hormone to stimulate growth. Tamoxifen is commonly used in both premenopausal and postmenopausal women.</li>
                                                    <li>Aromatase Inhibitors (AIs): Drugs like anastrozole and letrozole work by reducing estrogen production in postmenopausal women. AIs are often used after menopause because the ovaries stop producing estrogen, and these drugs block the small amounts produced in other tissues.</li>
                                                    <li>Ovarian Suppression: In premenopausal women, drugs like goserelin may be used to stop the ovaries from making estrogen temporarily.</li>
                                                </ul>

                                                <h5>For Prostate Cancer:</h5>
                                                <p>Prostate cancer often grows in response to androgens (male hormones like testosterone). Hormone therapy for prostate cancer, also called androgen deprivation therapy (ADT), works by reducing the levels of testosterone or blocking its effect on prostate cancer cells.</p>
                                                <ul>
                                                    <li>LHRH Agonists: Drugs like leuprolide or goserelin lower testosterone levels by stopping the production of the hormone.</li>
                                                    <li>Anti-androgens: Drugs like flutamide or bicalutamide block the action of testosterone on prostate cancer cells.</li>
                                                    <li>Orchiectomy: Surgical removal of the testicles, which drastically reduces testosterone levels.</li>
                                                </ul>

                                                <h4>When Is Hormone Therapy Used?</h4>
                                                <p>Hormone therapy can be used in several scenarios, depending on the type and stage of cancer: </p>
                                                <ul>
                                                    <li>Adjuvant Therapy: After surgery or radiation, hormone therapy helps reduce the risk of cancer returning by eliminating remaining hormone-dependent cancer cells.</li>
                                                    <li>Neoadjuvant Therapy: Before surgery, hormone therapy can shrink tumors, making them easier to remove.</li>
                                                    <li>Metastatic Cancer: In advanced cancer that has spread to other parts of the body, hormone therapy can help control the growth of cancer.</li>
                                                    <li>Prevention: In some high-risk patients, hormone therapy may be used to lower the chance of developing hormone-sensitive cancers.</li>
                                                </ul>

                                                <p>
                                                    Lung cancer treatment has evolved significantly over the years, with a focus on providing personalized care based on the type and stage of cancer. The standard care for lung cancer typically includes a combination of surgery, chemotherapy, radiation therapy, targeted therapy, and immunotherapy. These treatments are selected based on whether the lung cancer is classified as non-small cell lung cancer (NSCLC) or small cell lung cancer (SCLC), which are the two main types of the disease.
                                                </p>
                                                <h2 class="h1 mb-4">Surgery </h2>
                                                <p>Surgery is often the primary treatment for early-stage lung cancer. The goal is to remove the tumor and a margin of healthy tissue around it. Depending on the size and location of the tumor, the following procedures may be used:</p>
                                                <ol>
                                                    <li>Lobectomy: Removal of one of the five lobes of the lungs.</li>
                                                    <li>Pneumonectomy: Removal of an entire lung.</li>
                                                    <li>Segmentectomy or Wedge Resection: Removal of a small part of the lung containing the tumor.</li>
                                                    <p>Surgery is usually considered for non-small cell lung cancer (NSCLC) in stages I or II and sometimes in stage III if the cancer has not spread beyond the lung.</p>
                                                </ol>

                                                <h4>Chemotherapy </h4>
                                                <p>Chemotherapy involves using drugs to kill cancer cells throughout the body. It is often used in lung cancer as adjuvant therapy after surgery to eliminate any remaining cancer cells or as a primary treatment for more advanced lung cancer.</p>
                                                <ul>
                                                    <li>Adjuvant Chemotherapy: Administered after surgery to reduce the risk of recurrence.</li>
                                                    <li>Neoadjuvant Chemotherapy: Given before surgery to shrink tumors and make surgery more effective.</li>
                                                    <li>For Advanced Stages: Chemotherapy is often the first-line treatment for advanced NSCLC or small cell lung cancer (SCLC), which is generally more aggressive.</li>
                                                </ul>
                                                <p>Common chemotherapy drugs for lung cancer include cisplatin, carboplatin, paclitaxel, and etoposide.</p>

                                                <h4>Radiation Therapy </h4>
                                                <p>Radiation therapy uses high-energy X-rays or other forms of radiation to kill cancer cells or shrink tumors. It's often used when surgery is not possible or as part of a combined treatment approach.
                                                <ul>
                                                    <li>External Beam Radiation Therapy (EBRT): This is the most common type of radiation therapy for lung cancer, often used alongside chemotherapy (called chemoradiation). </li>
                                                    <li>Stereotactic Body Radiation Therapy (SBRT): A highly precise radiation therapy technique, often used for small, early-stage lung cancers that cannot be surgically removed.</li>

                                                </ul>
                                                </p>

                        <h2 class="h1 mb-4">What You Need to Know</h2>
                        <p>Type 1 diabetes, also known as juvenile diabetes, occurs when a child's immune system mistakenly attacks the insulin-producing cells in the pancreas. Without insulin, blood sugar levels rise dangerously high, causing various health complications. Recognizing the signs and symptoms early can lead to quicker diagnosis and treatment, helping to manage the condition effectively.
                        </p>
                        <h4>Key Signs and Symptoms:</h4>
                        <p>Increased Thirst and Frequent Urination:
                        <ul>
                          <li>Children with type 1 diabetes often feel extremely thirsty and may drink more than usual. This leads to frequent urination, especially at night. High blood sugar levels cause the kidneys to pull extra water out of the bloodstream, making the child feel dehydrated.</li>
                        </ul>
                        </p>
                        <p>Unexplained Weight Loss:
                        <ul>
                          <li>Despite eating normally, or even more than usual, children with type 1 diabetes may lose weight. This happens because the body is unable to properly use glucose for energy and begins to break down muscle and fat instead.</li>
                        </ul>
                        </p>
                        <p>Extreme Hunger:
                        <ul>
                          <li>Because their bodies can't process glucose for energy, children with type 1 diabetes often feel very hungry, even after eating a large meal.</li>
                        </ul>
                        </p>
                        <p>Fatigue:
                        <ul>
                          <li>Without insulin to help glucose enter the cells, the body lacks energy. As a result, children with type 1 diabetes may feel very tired or fatigued most of the time.</li>
                        </ul>
                        </p>
                        <p>Irritability or Mood Changes:
                        <ul>
                          <li>Sudden mood swings, irritability, or emotional changes can be a sign of fluctuating blood sugar levels.</li>
                        </ul>
                        </p>
                        <p>Fruity-Smelling Breath:
                        <ul>
                          <li>When the body begins to burn fat for energy instead of glucose, it produces chemicals called ketones. A high level of ketones in the blood can cause a fruity smell on the breath, which is a potential warning sign of diabetic ketoacidosis (a serious complication).</li>
                        </ul>
                        </p>
                        <p>Blurred Vision:
                        <ul>
                          <li>High blood sugar levels can cause fluid to be pulled from tissues, including the lenses of the eyes, which can lead to blurry vision.</li>
                        </ul>
                        </p>
                        <p>Slow-Healing Wounds or Frequent Infections:
                        <ul>
                          <li>Children with undiagnosed type 1 diabetes may experience slow healing of cuts or frequent infections, especially yeast infections or infections of the skin.</li>
                        </ul>
                        </p>
                        <p>Bedwetting:
                        <ul>
                          <li>Previously toilet-trained children who start wetting the bed again may have high blood sugar levels that cause increased urination, leading to bedwetting.</li>
                        </ul>
                        </p>
                        <h4>Preventing Type 1 Diabetes</h4>
                        <p>
                          Unfortunately, type 1 diabetes is not preventable because it is an autoimmune condition where the body's immune system attacks the insulin-producing beta cells in the pancreas. It often has a genetic component, meaning that children with a family history of the condition are at a higher risk. However, there are steps that can be taken to manage and reduce complications once a child is diagnosed:
                        </p>
                        <ol>
                          <li>Early Diagnosis: Recognizing the symptoms early and seeking prompt medical attention can prevent the severe complications of untreated type 1 diabetes, such as diabetic ketoacidosis.</li>
                          <li>Blood Sugar Management: After diagnosis, managing blood sugar levels with insulin therapy is crucial. Regular blood sugar monitoring and insulin injections help keep blood glucose within a target range.</li>
                          <li>Healthy Lifestyle: A balanced diet, regular physical activity, and monitoring carbohydrate intake can help manage blood sugar levels. While diet and exercise wonâ€™t prevent the disease, they are essential in maintaining stable glucose levels and avoiding complications.</li>
                          <li>Regular Medical Checkups: Routine visits to healthcare providers are essential for monitoring the condition, adjusting treatment plans, and screening for complications such as heart disease, eye problems, and nerve damage.</li>
                          <li>Emotional Support: Providing psychological and emotional support is vital, as managing a chronic condition like type 1 diabetes can be overwhelming for both children and their families. Engaging in diabetes education and support groups can help both parents and children better cope with the condition.</li>
                        </ol>

                        <h4>Conclusion</h4>
                        <p>While there is no way to prevent type 1 diabetes, understanding the symptoms and acting early can help parents and caregivers manage the condition effectively. Proper blood sugar control, a balanced diet, and regular medical care can ensure children with type 1 diabetes live healthy, active lives.</p>

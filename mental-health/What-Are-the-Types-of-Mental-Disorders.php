                                                <h2>6. Eating Disorders</h2>
                                                <p>Eating disorders involve unhealthy attitudes and behaviors related to food, eating, and body image. These disorders can lead to serious physical and emotional complications.</p>
                                                <h4>Common Eating Disorders:</h4>
                                                <ul>
                                                    <li>Anorexia Nervosa: An intense fear of gaining weight, leading to extreme food restriction and weight loss.</li>
                                                    <li>Bulimia Nervosa: Repeated episodes of binge eating followed by behaviors to prevent weight gain, such as vomiting, excessive exercise, or laxative use.</li>
                                                    <li>Binge-Eating Disorder: Frequent episodes of eating large amounts of food, often in secret, and feeling a lack of control during the binge.</li>
                                                </ul>
                                                <h4>Symptoms:</h4>
                                                <ul>
                                                    <li>Restriction of food intake (Anorexia)</li>
                                                    <li>Binge-eating followed by purging behaviors (Bulimia)</li>
                                                    <li>Eating large quantities of food in a short time (Binge-Eating Disorder)</li>
                                                    <li>Distorted body image</li>
                                                    <li>Extreme concern with body weight or shape</li>
                                                </ul>

                                                <h2>7. Post-Traumatic Stress Disorder (PTSD)</h2>
                                                <p>PTSD develops after experiencing or witnessing a traumatic event, such as war, natural disasters, physical assault, or accidents. It is characterized by flashbacks, nightmares, and severe anxiety related to the traumatic event.</p>
                                                <h4>Symptoms:</h4>
                                                <ul>
                                                    <li>Flashbacks: Reliving the traumatic event in vivid detail.</li>
                                                    <li>Nightmares: Disturbing dreams related to the trauma.</li>
                                                    <li>Hypervigilance: Being constantly on alert for danger.</li>
                                                    <li>Avoidance: Avoiding places, people, or activities that trigger memories of the trauma.</li>
                                                </ul>

                                                <h2>8. Neurodevelopmental Disorders</h2>
                                                <p>Neurodevelopmental disorders begin in childhood and affect how a person’s brain develops and functions. These disorders often impact learning, communication, and behavior.</p>
                                                <h4>Common Neurodevelopmental Disorders:</h4>
                                                <ul>
                                                    <li>Autism Spectrum Disorder (ASD): A developmental disorder that affects communication, behavior, and social interactions. People with ASD may have difficulty understanding social cues, engage in repetitive behaviors, or have specific interests.</li>
                                                    <li>Attention-Deficit/Hyperactivity Disorder (ADHD): A disorder characterized by difficulty paying attention, impulsivity, and hyperactivity.</li>
                                                    <li>Intellectual Disabilities: Conditions that result in below-average intellectual functioning and limitations in adaptive behaviors such as communication, self-care, and social skills.</li>
                                                </ul>
                                                <h4>Symptoms:</h4>
                                                <ul>
                                                    <li>Difficulty with social interactions (ASD)</li>
                                                    <li>Hyperactivity, impulsivity, or inattentiveness (ADHD)</li>
                                                    <li>Delayed development in learning and problem-solving (Intellectual Disabilities)</li>
                                                </ul>

                                                <h2>Conclusion</h2>
                                                <p>There are many types of mental health disorders, each affecting people in different ways. These disorders can range from anxiety and mood disorders to more severe conditions like psychotic disorders and personality disorders. While the symptoms and challenges of each mental health disorder vary, many of these conditions can be effectively treated or managed through a combination of therapy, medication, and support. Understanding the types of mental health disorders is the first step toward getting the right help and treatment for those who are affected.</p>


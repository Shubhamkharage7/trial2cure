        <script>
            function handleUserTypeChange() {
                var userType = document.getElementById('form-select').value;

                // Hide all additional fields initially
                document.getElementById('company-field').style.display = 'none';
                document.getElementById('location-field-cro').style.display = 'none';
                document.getElementById('hospital-field').style.display = 'none';
                document.getElementById('location-field-hp').style.display = 'none';

                if (userType === 'CRO/CRA') {
                    // Show company and location fields for CRO/CRA
                    document.getElementById('company-field').style.display = 'block';
                    document.getElementById('location-field-cro').style.display = 'block';
                } else if (userType === 'Healthcare Professional') {
                    // Show hospital and location fields for Healthcare Professionals
                    document.getElementById('hospital-field').style.display = 'block';
                    document.getElementById('location-field-hp').style.display = 'block';
                }
            }
